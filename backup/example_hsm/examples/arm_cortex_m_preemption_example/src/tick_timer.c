void tickTimer_init(void)
{
  SYST_CSR = 0U;
  SYST_RVR = SYSTICK_RELOAD_1_MS;
  SYST_CVR = 0U;
  SYST_CSR = SYST_CSR_CLKSOURCE | SYST_CSR_TICKINT | SYST_CSR_ENABLE;
}

void SysTick_Handler(void)
{
  EDF_timeEvent_tick(0U);
  EDF_ISRExit();
}
